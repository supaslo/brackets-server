package com.overboardsb.brackets.model;

public class BracketRequest {

    private String name;

    private String[] rounds;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String[] getRounds() {
        return rounds;
    }

    public void setRounds(String[] rounds) {
        this.rounds = rounds;
    }

    
}
